package com.explore.android.mobile.model;

public class VatToCustomer {

	private String vatToId;
	private String vatToName;
	private String customerDeptId;

	public String getVatToId() {
		return vatToId;
	}

	public void setVatToId(String vatToId) {
		this.vatToId = vatToId;
	}

	public String getVatToName() {
		return vatToName;
	}

	public void setVatToName(String vatToName) {
		this.vatToName = vatToName;
	}

	public String getCustomerDeptId() {
		return customerDeptId;
	}

	public void setCustomerDeptId(String customerDeptId) {
		this.customerDeptId = customerDeptId;
	}

}
