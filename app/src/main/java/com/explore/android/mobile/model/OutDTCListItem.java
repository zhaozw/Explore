package com.explore.android.mobile.model;

public class OutDTCListItem {
	
	private String partno;
	
	private String transportQty1S;
	
	private String outQty;
	
	private String soPrice;

	public String getPartno() {
		return partno;
	}

	public void setPartno(String partno) {
		this.partno = partno;
	}

	public String getTransportQty1S() {
		return transportQty1S;
	}

	public void setTransportQty1S(String transportQty1S) {
		this.transportQty1S = transportQty1S;
	}

	public String getOutQty() {
		return outQty;
	}

	public void setOutQty(String outQty) {
		this.outQty = outQty;
	}

	public String getSoPrice() {
		return soPrice;
	}

	public void setSoPrice(String soPrice) {
		this.soPrice = soPrice;
	}

}
