package com.explore.android.mobile.model;

public class ExConstant {

	private int constantId;
	private String constantType;
	private String constantName;
	private String constantValue;
	private String category1;
	private String category2;
	private int constantOrder;

	public int getConstantId() {
		return constantId;
	}

	public void setConstantId(int constantId) {
		this.constantId = constantId;
	}

	public String getConstantType() {
		return constantType;
	}

	public void setConstantType(String constantType) {
		this.constantType = constantType;
	}

	public String getConstantName() {
		return constantName;
	}

	public void setConstantName(String constantName) {
		this.constantName = constantName;
	}

	public String getConstantValue() {
		return constantValue;
	}

	public void setConstantValue(String constantValue) {
		this.constantValue = constantValue;
	}

	public String getCategory1() {
		return category1;
	}

	public void setCategory1(String category1) {
		this.category1 = category1;
	}

	public String getCategory2() {
		return category2;
	}

	public void setCategory2(String category2) {
		this.category2 = category2;
	}

	public int getConstantOrder() {
		return constantOrder;
	}

	public void setConstantOrder(int constantOrder) {
		this.constantOrder = constantOrder;
	}

}
