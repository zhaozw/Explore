public class ProductKV {

	private String productKVId;

	private String productId;

	private String productKeyId;

	private String productValue;

	public String getProductKVId() {
		return productKVId;
	}

	public void setProductKVId(String productKVId) {
		this.productKVId = productKVId;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getProductKeyId() {
		return productKeyId;
	}

	public void setProductKeyId(String productKeyId) {
		this.productKeyId = productKeyId;
	}

	public String getProductValue() {
		return productValue;
	}

	public void setProductValue(String productValue) {
		this.productValue = productValue;
	}
	
}
